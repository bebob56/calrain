<p><b>NOTE:</b> When most weather reporting sources cite weather in Los Angeles (without specifying where, exactly, in Los Angeles), these numbers are typically for the Downtown Los Angeles weather station (actually located on the USC campus).</p>

<p class="comment-box">This last rainfall season (2020-2021) was the <a href="http://www.laalmanac.com/weather/we08ba.php">seventh driest season for Los Angeles</a> since rainfall records began being kept in 1877.</p>
<br><br>

<a href="http://www.laalmanac.com/images3/chart_rainfall_LA_1887_2020.jpg" target="_blank">
<img src="../images3/chart_rainfall_LA_1887_2020b.jpg" alt="Downtown LA Rainfall Chart, 1887-2020" title="Downtown LA Rainfall Chart, 1887-2020"></a>
<p>Downtown Los Angeles historical seasonal rainfall chart, 1877-2020. <b>Click on image for larger image</b>.</p>
<br>

<p class="comment-box">In July 1999, for the start of the 1999-2000 rain season, <b>the Downtown Los Angeles weather station was relocated</b> from the upper level of a two-story parking structure at the Los Angeles Department of Water and Power (DWP) building to a ground-level, park-like setting on the University of Southern California (USC) campus. This was the first time that the downtown station was located outside the city center of Los Angeles. The move was part of a nationwide effort by the National Weather Service to locate all weather stations at ground-level with more natural surroundings. The USC location is almost four miles closer to the coast and about 90 feet lower in elevation than the former DWP location. The relocation naturally drew <a href="https://www.jpl.nasa.gov/news/news.php?feature=1273" target="_blank">some concern by meteorologists</a>, regarding the continuity of overall historic weather data trends for Downtown Los Angeles station.</p>
<br><br>

<p><b>Overall average for seasons 1877 through 2020: 14.67 inches*</b></p>

